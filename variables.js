/*  Data types provided by js  */
//  undefined, null, boolean, string, symbol, number and object


// variable : a memory location which holds data and have a name

// var keyword
// var can be used throughout your program
var myName = "sidharth";
myName = 8;

// let keyword
// only be used within the scope of it's declaration
let ourName = "freecodecamp";

// const keyword
// value of const cannot be changed
const pi = 3.14;

// printing variables
console.log(myName, ourName, pi);



